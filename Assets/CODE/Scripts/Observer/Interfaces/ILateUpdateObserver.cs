namespace CODE.Scripts.Observer.Interfaces
{
	/// <summary>
	/// Интерфейс для объектов, которые должны получать вызовы в фазе <c>LateUpdate</c>
	/// через менеджер <c>LateUpdateSceneManager</c>.
	/// </summary>
	/// <remarks>
	/// Метод <see cref="CoreLateUpdate"/> вызывается после всех <c>Update</c>-вызовов,
	/// поэтому он подходит для логики, которая должна выполняться после расчёта состояний
	/// другими системами (например, позиционирование камеры, корректировки анимаций).
	///
	/// Контракт и рекомендации:
	/// - Метод должен быть быстрым и не выделять память, чтобы избежать просадок FPS.
	/// - Обработку ошибок лучше локализовать внутри метода, чтобы одна ошибка не
	///   прервала выполнение остальных подписчиков.
	/// </remarks>
	public interface ILateUpdateObserver
	{
		/// <summary>
		/// Основной метод LateUpdate, вызываемый менеджером <c>LateUpdateSceneManager</c>.
		/// </summary>
		void CoreLateUpdate();
	}
}