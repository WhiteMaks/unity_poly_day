namespace CODE.Scripts.Observer.Interfaces
{
	/// <summary>
	/// Интерфейс для объектов, которые должны получать вызовы обновления каждый кадр
	/// через менеджер <c>UpdateSceneManager</c>.
	/// </summary>
	/// <remarks>
	/// Реализация этого интерфейса позволяет перенести нагрузку с множества
	/// <c>MonoBehaviour.Update</c> в централизованный менеджер, что уменьшает
	/// количество вызовов Unity и потенциально повышает производительность.
	///
	/// Контракт:
	/// - Метод <see cref="CoreUpdate"/> вызывается менеджером в каждом кадре.
	/// - Метод не должен генерировать исключений: если требуется обработка ошибок,
	///   лучше изолировать её внутри самого метода, чтобы не нарушать цикл менеджера.
	/// - Желательно избегать аллокаций и тяжёлых операций внутри <see cref="CoreUpdate"/>
	///   по соображениям производительности.
	/// </remarks>
	public interface IUpdateObserver
	{
		/// <summary>
		/// Основной метод обновления, эквивалентный логике, которую обычно размещают
		/// в <c>MonoBehaviour.Update</c>. Вызывается менеджером <c>UpdateSceneManager</c>.
		/// </summary>
		void CoreUpdate();
	}
}