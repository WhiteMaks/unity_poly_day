namespace CODE.Scripts.Observer.Interfaces
{
	/// <summary>
	/// Интерфейс для объектов, которые должны получать вызовы в фазе <c>FixedUpdate</c>
	/// через менеджер <c>FixedUpdateSceneManager</c>.
	/// </summary>
	/// <remarks>
	/// Этот интерфейс предназначен для физически-ориентированной логики, которая должна
	/// выполняться с фиксированным шагом симуляции (например, интеграция скоростей,
	/// применение сил, расчёт столкновений вне встроенной физики Unity и т.д.).
	///
	/// Контракт:
	/// - <see cref="CoreFixedUpdate"/> вызывается менеджером на каждом фиксированном шаге.
	/// - Метод должен быть детерминированным и быстрым; избегайте работы с Unity API,
	///   зависящей от кадра, если это может привести к рассинхронизации.
	/// </remarks>
	public interface IFixedUpdateObserver
	{
		/// <summary>
		/// Основной метод фиксированного обновления, вызываемый менеджером <c>FixedUpdateSceneManager</c>.
		/// </summary>
		void CoreFixedUpdate();
	}
}